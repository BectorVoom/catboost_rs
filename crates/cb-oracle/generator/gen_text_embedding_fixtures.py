EMBEDDING_CALCER_SPECS = {
    "LDA": "LDA:reg=0.05:likelihood=true",
    "KNN": "KNN:k=3",
}

TEXT_CALCERS = ["BoW", "NaiveBayes", "BM25"]
EMBEDDING_CALCERS = ["LDA", "KNN"]


