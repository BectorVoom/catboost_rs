import argparse
import json
import os
import re
import subprocess
import sys
import tempfile
from pathlib import Path

import numpy as np

GENERATOR_DIR = Path(__file__).resolve().parent
FIXTURES = GENERATOR_DIR.parent / "fixtures"
TEXT_TOKENIZER_DIR = FIXTURES / "text_tokenizer"
TEXT_CALCERS_DIR = FIXTURES / "text_calcers"
EMBEDDING_CALCERS_DIR = FIXTURES / "embedding_calcers"
INPUTS_DIR = FIXTURES / "text_embedding_inputs"

CATBOOST_VERSION = "1.2.10"
RANDOM_SEED = 20260618
DEPTH = 2
ITERATIONS = 5
LEARNING_RATE = 0.3
LEAF_ESTIMATION_ITERATIONS = 1
EMBED_DIM = 4

# Data-dependent default (options_helper.cpp:394-401): poolSize < 1000 -> 1 else 5.
# Our learn pool is intentionally small, so this PINS to 1. Asserted at gen time.
OCCURRENCE_LOWER_BOUND_PINNED = 1
MAX_DICTIONARY_SIZE_PINNED = 50000

# ---------------------------------------------------------------------------
# FROZEN CORPUS. A small, deterministic, binary-labelled text+embedding pool. The
# vocabulary is deliberately tiny and repetitive so the dictionary, the per-class
# token frequencies (NaiveBayes/BM25), and the per-class embedding clouds (LDA/KNN)
# are all well-conditioned and reproducible. ByDelimiter (space) tokenization +
# lowercasing are upstream defaults (D-02).
# ---------------------------------------------------------------------------
_POS_TEXTS = [
    "good great movie",
    "good film great",
    "great good film",
    "great great good",
    "good good great",
    "good movie great film",
    "great wonderful good",
    "good great great",
]
_NEG_TEXTS = [
    "bad awful film",
    "awful movie bad",
    "bad bad awful",
    "awful awful bad",
    "bad film awful",
    "awful bad movie film",
    "bad terrible awful",
    "awful bad bad",
]


def _build_corpus() -> tuple[list[str], list[list[float]], list[int]]:
    """Deterministic interleaved text + embedding + binary-label corpus."""
    rng = np.random.default_rng(RANDOM_SEED)
    texts: list[str] = []
    labels: list[int] = []
    embeds: list[list[float]] = []
    # Two well-separated embedding clouds (one per class) so LDA/KNN have signal.
    pos_center = np.array([1.0, 1.0, -1.0, -1.0])
    neg_center = np.array([-1.0, -1.0, 1.0, 1.0])
    for pos_t, neg_t in zip(_POS_TEXTS, _NEG_TEXTS):
        texts.append(pos_t)
        labels.append(1)
        embeds.append((pos_center + 0.25 * rng.normal(size=EMBED_DIM)).tolist())
        texts.append(neg_t)
        labels.append(0)
        embeds.append((neg_center + 0.25 * rng.normal(size=EMBED_DIM)).tolist())
    return texts, embeds, labels


LEARN_ROWS = len(_POS_TEXTS) + len(_NEG_TEXTS)  # == 16, strictly < 1000 -> OLB=1


def _make_pool(texts, embeds, labels):
    import pandas as pd
    from catboost import Pool

    df = pd.DataFrame({"text0": texts})
    df["emb0"] = [list(map(float, row)) for row in embeds]
    return Pool(
        data=df,
        label=labels,
        text_features=["text0"],
        embedding_features=["emb0"],
    )


# ByDelimiter (space) tokenizer + frequency Word dictionary; BoW additionally over
# a BiGram dictionary (Pitfall 4: default classification BoW = [Bigram, Word]).
def _text_processing(calcer: str) -> dict:
    tokenizers = [
        {"tokenizer_id": "Space", "separator_type": "ByDelimiter", "delimiter": " ", "lowercasing": "true"}
    ]
    dictionaries = [
        {
            "dictionary_id": "Word",
            "gram_order": "1",
            "occurrence_lower_bound": str(OCCURRENCE_LOWER_BOUND_PINNED),
            "max_dictionary_size": str(MAX_DICTIONARY_SIZE_PINNED),
        },
        {
            "dictionary_id": "Bigram",
            "gram_order": "2",
            "occurrence_lower_bound": str(OCCURRENCE_LOWER_BOUND_PINNED),
            "max_dictionary_size": str(MAX_DICTIONARY_SIZE_PINNED),
        },
    ]
    dicts = {"BoW": ["Bigram", "Word"], "NaiveBayes": ["Word"], "BM25": ["Word"]}
    feature_processing = [
        {
            "feature_calcers": [calcer],
            "dictionaries_names": dicts[calcer],
            "tokenizers_names": ["Space"],
        }
    ]
    return {
        "feature_processing": {"default": feature_processing},
        "dictionaries": dictionaries,
        "tokenizers": tokenizers,
    }


