#!/usr/bin/env python3
"""OFFLINE text & embedding per-stage fixture generator (Phase 6.5, FEAT-01/02).

RUN-ONCE / COMMIT discipline (D-09): a BUILD-TIME tool run OUTSIDE CI on a machine
with catboost==1.2.10 importable. It writes committed frozen `.npy` + `model.json`
+ `config.json` fixtures; CI only READS them. NEVER invoked from CI. Re-run by
hand to (re)generate a fixture, then COMMIT.

This is Wave 0 for Phase 6.5: it produces the per-stage SINGLE-THREAD ground truth
(thread_count=1, NOT the -T 4 canondata — RESEARCH A6) that the later tokenizer /
calcer slices (Plans 02-07) oracle against, plus it freezes the D-01 tokenizer /
dictionary instrumented corpus captured from the D-07 instrumented trainer
(Task 1).

CALCERS (one pinned model.json per calcer, thread_count=1, seed pinned):
    Text  (FEAT-01): BoW, NaiveBayes, BM25  -> fixtures/text_calcers/<calcer>/
    Embed (FEAT-02): LDA, KNN               -> fixtures/embedding_calcers/<calcer>/

PER-STAGE FIXTURES per calcer dir (the established 06.3/06.4 pattern, but as .npy
because text/embedding models can ONLY be exported in `cbm` format — upstream
`model_exporter.cpp:152` rejects JSON export when text/embedding features exist, so
the `model.json` path the borders/leaf calcer fixtures use elsewhere is impossible
here; the live single-thread trainer's per-stage arrays are frozen directly):
    splits.npy        per-tree split BORDER value, tree order (Stage::Splits)
    leaf_values.npy   per-tree leaf values, tree order   (Stage::LeafValues)
    leaf_weights.npy  per-tree leaf weights, tree order   (audit / partition)
    staged.npy        per-iteration staged RawFormulaVal  (Stage::StagedApprox)
    predictions.npy   final RawFormulaVal predictions     (Stage::Predictions)
    model.cbm         the binary model (full-fidelity reload; text/embedding-safe)
    config.json       pinned {seed, catboost_version, thread_count} (cb_oracle schema)
    params.json       the FULL pinned param set (audit; superset of config.json)

TOKENIZER/DICTIONARY D-01 CORPUS (fixtures/text_tokenizer/):
    captured from the instrumented trainer's CB_INSTRUMENT_LOG dump (Task 1) over
    the SAME corpus: token_stream / dict_ids / ttext events frozen as JSON +
    config.json. Generated by `--tokenizer-corpus` AFTER building the instrumented
    trainer; it shells out to the staged instrumented `_catboost.so`.

CORPUS (A4 — OLB boundary is strict `< 1000`):
    The learn pool is a SMALL deterministic text+embedding corpus of LEARN_ROWS
    rows. LEARN_ROWS < 1000 -> the data-dependent OccurrenceLowerBound resolves to
    1 (options_helper.cpp:394-401 `poolSize < 1000 ? 1 : 5`). We RECORD the exact
    learn-pool row count and PIN OccurrenceLowerBound = 1 explicitly in every
    config (no assumption — the count is asserted at generation time).

Run (OFFLINE, after ensuring `.venv` has catboost==1.2.10):
    .venv/bin/python gen_text_embedding_fixtures.py --inputs        # write corpus inputs
    .venv/bin/python gen_text_embedding_fixtures.py --calcers       # all 5 calcer fixtures
    .venv/bin/python gen_text_embedding_fixtures.py --tokenizer-corpus \
        --instr-pkg /tmp/cb_build313/instr_pkg                      # D-01 instrumented corpus
    .venv/bin/python gen_text_embedding_fixtures.py --all --instr-pkg /tmp/cb_build313/instr_pkg
"""
