_INSTR_FIT_SNIPPET = r'''
import json, sys
import numpy as np
import pandas as pd
from catboost import Pool, CatBoost

texts = json.loads(sys.argv[1])
embeds = json.load(open(sys.argv[2]))
labels = json.load(open(sys.argv[3]))
df = pd.DataFrame({"text0": texts})
df["emb0"] = [list(map(float, r)) for r in embeds]
pool = Pool(data=df, label=labels, text_features=["text0"], embedding_features=["emb0"])
params = json.load(open(sys.argv[4]))
model = CatBoost(params)
model.fit(pool)
print("INSTR_FIT_OK trees", model.tree_count_)
'''


def gen_tokenizer_corpus(instr_pkg: str) -> None:
    """Capture + freeze the D-01 instrumented token/dict/ttext corpus (D-07)."""
    instr_pkg_path = Path(instr_pkg)
    so = instr_pkg_path / "catboost" / "_catboost.so"
    if not so.exists():
        raise SystemExit(
            f"instrumented trainer not found at {so}; run build_instrumented_trainer.sh first"
        )
    if not (INPUTS_DIR / "meta.json").exists():
        write_inputs()
    texts, embeds, labels = _build_corpus()

    TEXT_TOKENIZER_DIR.mkdir(parents=True, exist_ok=True)
    venv_py = str((GENERATOR_DIR.parent.parent.parent / ".venv" / "bin" / "python"))

    with tempfile.TemporaryDirectory() as tmp:
        tmpd = Path(tmp)
        (tmpd / "embeds.json").write_text(json.dumps(embeds), encoding="utf-8")
        (tmpd / "labels.json").write_text(json.dumps(labels), encoding="utf-8")
        fit_script = tmpd / "fit.py"
        fit_script.write_text(_INSTR_FIT_SNIPPET, encoding="utf-8")

        # Two fits so the frozen corpus covers ALL hook categories:
        #   BoW over [Bigram, Word] exercises BOTH the unigram and bigram dictionaries
        #     (token_stream / dict_ids / ttext) AND, via the default embedding calcers,
        #     lda_projection / knn_neighbors.
        #   NaiveBayes is an ONLINE text estimator (D-03), so it additionally fires
        #     online_order + calcer_encoding (the BoW non-online path does not).
        events: dict[str, list] = {}
        per_calcer: dict[str, dict[str, int]] = {}
        for calcer in ("BoW", "NaiveBayes"):
            params = _base_params()
            params["text_processing"] = _text_processing(calcer)
            (tmpd / "params.json").write_text(json.dumps(params), encoding="utf-8")
            log_path = tmpd / f"instr_{calcer}.jsonl"
            env = dict(os.environ)
            env["CB_INSTRUMENT_LOG"] = str(log_path)
            env["PYTHONPATH"] = str(instr_pkg_path)
            proc = subprocess.run(
                [venv_py, str(fit_script), json.dumps(texts),
                 str(tmpd / "embeds.json"), str(tmpd / "labels.json"), str(tmpd / "params.json")],
                env=env, capture_output=True, text=True,
            )
            if proc.returncode != 0:
                raise SystemExit(f"instrumented {calcer} fit failed (rc={proc.returncode}):\n{proc.stderr[-2000:]}")
            print(f"  instrumented {calcer} fit:", proc.stdout.strip())
            calcer_counts: dict[str, int] = {}
            with open(log_path, encoding="utf-8") as fh:
                for line in fh:
                    line = line.strip()
                    if not line:
                        continue
                    try:
                        obj = json.loads(line)
                    except json.JSONDecodeError:
                        continue
                    ev = obj.get("event")
                    if ev is None:
                        continue
                    obj["_calcer"] = calcer
                    events.setdefault(ev, []).append(obj)
                    calcer_counts[ev] = calcer_counts.get(ev, 0) + 1
            per_calcer[calcer] = calcer_counts

    # Freeze ALL 7 instrumented categories: the D-01 tokenizer/dictionary ground
    # truth (token_stream / dict_ids / ttext) plus the online_order / calcer_encoding
    # / lda_projection / knn_neighbors the later calcer oracles (Plans 02-07) consume.
    ALL_CATS = (
        "token_stream", "dict_ids", "ttext",
        "online_order", "calcer_encoding",
        "lda_projection", "knn_neighbors",
    )
    captured = {}
    for cat in ALL_CATS:
        captured[cat] = events.get(cat, [])
        (TEXT_TOKENIZER_DIR / f"{cat}.json").write_text(
            json.dumps(events.get(cat, []), indent=2), encoding="utf-8"
        )
    summary = {
        "catboost_version": CATBOOST_VERSION,
        "learn_rows": LEARN_ROWS,
        "occurrence_lower_bound_pinned": OCCURRENCE_LOWER_BOUND_PINNED,
        "fit_calcers": list(per_calcer.keys()),
        "per_calcer_event_counts": per_calcer,
        "hook_event_counts": {k: len(v) for k, v in events.items()},
        "captured_categories": {k: len(v) for k, v in captured.items()},
        "source": "instrumented catboost 1.2.10 trainer (D-07), CB_INSTRUMENT_LOG; 2 fits (BoW + NaiveBayes)",
    }
    (TEXT_TOKENIZER_DIR / "summary.json").write_text(json.dumps(summary, indent=2), encoding="utf-8")
    (TEXT_TOKENIZER_DIR / "config.json").write_text(json.dumps(_cb_oracle_config(), indent=2), encoding="utf-8")
    print(f"  froze D-01 tokenizer corpus under {TEXT_TOKENIZER_DIR}: {summary['captured_categories']}")
    # Escalate-don't-weaken: every one of the 7 instrumented categories MUST be
    # non-empty across the two fits or the corpus is incomplete — never fabricate.
    missing = [c for c in ALL_CATS if not captured[c]]
    if missing:
        raise SystemExit(
            f"D-01 corpus INCOMPLETE — empty hook categories: {missing}. "
            f"Hook coverage: {summary['hook_event_counts']}. "
            "The instrumented trainer did not emit these; do NOT fabricate — fix the hook."
        )


